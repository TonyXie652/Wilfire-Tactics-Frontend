export default function SimulationPage() {
  const navigate = useNavigate();
  const [timeMs, setTimeMs] = useState(() => performance.now());
  const [fireCells, setFireCells] = useState<FireCell[]>(initialFire);
  const [isPaused, setIsPaused] = useState(true);

  // ── 风力参数（后续接入 UI 滑块控制） ──
  const [wind] = useState<WindConfig>({
    angleDeg: 45,           // 默认东北风
    speed: 0.6,             // 中等风速
    baseSpreadChance: 0.09, // 基础蔓延概率(使用朋友的新配置)
  });

  // 高频动画循环 (用来驱动呼吸效果)
  useEffect(() => {
    let raf = 0;
    let last = 0;

    const loop = (t: number) => {
      // 保持约 30fps 的刷新率
      if (t - last > 33) {
        last = t;
        setTimeMs(t);
      }
      raf = requestAnimationFrame(loop);
    };

    raf = requestAnimationFrame(loop);
    return () => cancelAnimationFrame(raf);
  }, []);

  // 低频逻辑循环 (用来驱动火灾蔓延)
  useEffect(() => {
    if (isPaused) return;

    const timer = window.setInterval(() => {
      setFireCells((prev) => stepFireSpread(prev, wind));
    }, 1000);

    return () => window.clearInterval(timer);
  }, [isPaused, wind]);

  // 【核心修改点】：在这里计算脉冲，并传给火灾图层
  const layers = useMemo(() => {

    const pulseRatio = 1.0 + Math.sin(timeMs / 300) * 0.02;


    return [
      ...makeRoadLayers(scenario),
      // 将算好的 pulseRatio 作为参数传进去！
      ...makeFireLayer(fireCells, { pulseRatio }),
      ...makeAgentsLayer(agents),
      ...makeSafePointsLayer(scenario.safePoints, { timeMs }),
    ];
  }, [timeMs, fireCells]);

  return (
    <div style={{ height: "100vh", width: "100vw", position: "relative" }}>
      {/* 顶部按钮 UI */}
      <button
        onClick={() => navigate("/")}
        style={{
          position: "absolute",
          top: 20,
          left: 20,
          zIndex: 10,
          padding: "10px 16px",
          fontSize: "14px",
          background: "#1f2937",
          color: "white",
          border: "1px solid #444",
          borderRadius: "6px",
          cursor: "pointer",
        }}
      >
        Back Home
      </button>

      <div
        style={{
          position: "absolute",
          top: 20,
          right: 20,
          zIndex: 10,
          display: "flex",
          gap: "10px",
          alignItems: "center"
        }}
      >
        <button
          onClick={() => setIsPaused((p) => !p)}
          style={{
            padding: "10px 16px",
            fontSize: "14px",
            background: isPaused ? "#2e7d32" : "#b71c1c",
            color: "white",
            border: "1px solid #444",
            borderRadius: "6px",
            cursor: "pointer",
          }}
        >
          {isPaused ? "Resume Fire" : "Pause Fire"}
        </button>
      </div>

      <MapView layers={layers} />
    </div>
  );
}